package io.seldon.api.resource;

import java.io.Serializable;



/**
 * @author claudio
 */


public abstract class ResourceBean implements Serializable, Comparable<ResourceBean> {
	
	public  int compareTo(ResourceBean o) { return this.toKey().compareTo(o.toKey()); }
	public abstract String toKey();
	public String toLog() { return null; };
	
}
